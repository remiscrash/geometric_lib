def area(a, b):
#Возвращает площадь прямоугольника
#Параметры: a(int) & b(int) - стороны прямоугольника
#Возвращаемое значение ф-ции: a*b - искомая площадь фигуры
	return a * b
def perimeter(a, b):
#Возвращает периметр прямоугольника
#Параметры: a(int) & b(int) - стороны прямоугольника
#Возвращаемое значение ф-ции: 2*a+2*b - искомый периметр прямоугольника
	return 2*a + 2*b