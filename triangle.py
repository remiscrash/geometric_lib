def area(a, h):
#Возвращает площадь треугольника
#Параметры: a(int)-основание треугольника h(int)- высота треугольника
#Возвращааемое значение: a * h / 2 - искомая площадь треугольника
	return a * h / 2
def perimeter(a, b, c):
# Возвращает площадь треугольника
# Параметры: a(int), b(int), c(int) - стороны треугольника
# Возвращааемое значение: a + b + c - искомый периметр треугольника
	return a + b + c